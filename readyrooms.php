<div class="row mt-5">
      <div class="col-12 col-md-3"><h4>Approach</h4></div>
      <div class="col-md-9">
          <h5>Discovery</h5>
           <p>With over 10 years in the web conferencing space, we knew some of the pain points associated with conference room technology and starting and joining online meetings. We  supplemented our existing knowledge with market research reports and meetings with analysts. We bought our competitors in-room solutions and set up labs across our offices to get used to them. And we remembered that not everyone is as passionate about conferencing as we are, so we used interviews and surveys to ensure we were honing in the value proposition and vernacular of our potential customers. </p>

           <p>The product team already had well-established user personas for our product, but we needed to dig into understanding a different type of user for this product: the person who would be responsible for setting up and supporting the hardware. We went through an internal exercice developing an empathy map for IT buyers/managers, including bringing in our own Head of IT to speak with us about his values and needs when evaluating solutions. This had a LOT of benefits for the team, not only in designing solutions our customers would be comfortable buying, but just in understanding why IT always seemed to block simple requests! Once we had this initial empathy map, we continued to refer to it through our design and customer feedback sessions, to update or refine as we got more information.</p>

           <img src="img/rr-empathymap.png" class="img-fluid" /> 


           <p>As well, I did several rounds of client visits, to observe their existing spaces and practices. </p>
           <p>As expected, the site visits were eye-opening. I visited tiny conference rooms where the meeting organizer would walk in with a borrowed projector and set it up facing a window, and vast open spaces with terrible acoustics. In one location, I was at first intimidated by the expensive professionally installed conferencing equipment, until I was told no one knew how to use it. </p>

            <h5>Design</h5>
           <p>We recognized early on that we had four key moments to address, and three personas to consider. We kept these in mind as we conducted our research and ideation, to ensure we weren't making unintentional tradeoffs.</p>
           <img src="img/rr-mural.png" class="img-fluid" /><br />

              <p><strong>The fundamental tension of this project was security versus convenience.</strong></p> 

              <p>Was the conference appliance the central to the experience,  or secondary? However we conceived the role of the appliance would impact the rest of the experience.</p> 

              <img src="img/rr-matrixsketch.png" class="img-fluid" /><br />


            <h5>Testing</h5>
              <p>In the spirit of getting customer input as soon as we could, the first round of the prototype did not include scheduling. We wanted to be sure that we were on the right track with the experience to join and in-room experience.
                <p>Turns out, they liked it that way. Our  "join" experience won them over in its simplicity, and scheduling may have only complicated the solution and deterred trial. </p>
                <p>Allowing users to include the appliance in the meeting at the exact time they needed it gave them an immediate reward for their effort. It was contextual, and we could use cues in the meeting room to assist them in doing so. Compare this to scheduling in advance, when users would be expected to anticipate and prepare for something at a different location and time. It sounds silly, but scheduling forces the user to expend effort in advance, well apart from when they'll see the benefits of this. This can decrease trial and usage. </p>


<p>In all, we engaged in the following activities during this project:</p>

