        <p>In November 2017, a fellow researcher and I conducted 12 Jobs to be Done "Switch"-style interviews. Focusing on people who had been actively involved in the purchasing decision within the past 18 months, we spoke with:</p>
<ul><li>1 unconverted prospect</li>
<li>3 churned customers (one churned for seasonality and plans to return to client)</li>
<li>8 current customers</li></ul>

<p>These interviewees were asked about the circumstances leading up to their decision to switch products, and how their new solution helps them with their work activities in a way the former solution could not.</p> 

<p>Considering the impact of the interviewees comments as a series of "forces" acting upon them, we were able to identify factors that could be manipulated to promote our desired behavior with other customers. </p>

          <img src="img/se-fourforces.png" class="img-fluid" />
          <img src="img/se-forcestable.png" class="img-fluid" />

<p>Specifically, the 'push' forces identified could be used by the marketing and sales teams to proactively identify prospects.</p>

<p>After the interviews were complete, we performed a synthesis where we identified different clusters of needs among our respondents. This enabled us to identify two major segments of users with distinct needs and challenges. One of our recommendations was thus to position the product differently for these two markets; focusing on their particular needs, concerns and measures of success.</p>


