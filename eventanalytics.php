<div class="row mt-4">
      <div class="col-12 col-md-3"><h4>Outcomes</h4></div>
      <div class="col-md-9"><p>Three big results came out of this research:</p>

<ol><li>We did NOT move forward with the concept our stakeholder had been so bullish on. He thanked us for saving the company a lot of money and time.</li>
<li>We decided to further explore the concept that had emerged through the second round of qualitative interviews.</li>
<li>The entire product organization realized how little we knew about our customers’ Jobs to be Done and what truly motivated them.</li>
</ol>

<p>Number three was a huge win for my team, as it helped our internal customers understand how we could be beneficial to them. As in many product organizations, they were very often focused on optimizing how best to build things, and through a better understanding of the customers’ Jobs to be Done, we were able to provide the context and direction to help them understand what best to build.</p>

<p>Often when we look at product successes, we look at launches. In this case, we reveled in what we had saved the company in opportunity cost. The feature that we had planned would have been extremely expensive, and by talking to our customers and better understanding what was important to them, we were able to better invest our time and resources elsewhere.</p>
 
