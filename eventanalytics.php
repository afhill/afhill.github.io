      <div class="col-md-9">
        <p>Our dedicated innovation team conducted a design sprint, with the manager of the Webinar line of business serving as our "Decider". She did not actively participate during the week, which was a change we made to further design sprints we ran for other teams. Instead, we were dedicated to the week's activities, and then she would come in to review and give feedback on our progress. 

          <ol>
             <li>Map users existing process</li>
            <li>Ideate and brainstorm concepts</li>
            <li>Create a prototype to guide discussion</li>
            <li>Elicit their feedback on the concepts </li>
          </ol>

          <h4>Mapping the existing process</h4>
          <p>The Monday activity of mapping the process was helpful for our team, as we brought in experts as well as customers to walk us through their work. Once everyone in the sprint had a shared understanding of the workflow, we honed in on the time immediately after a marketer had held their webinar. </p>

          <img src="img/ds-sketches.png" class="img-fluid" /> 

          <h4 class="mt-3">Ideate and brainstorm concepts</h4>
          <p>Tuesday is always my favourite day of design sprints. In the morning, everyone brought different related concepts and sources of inspiration. Coming together to share these different thoughts was wonderful: the team identified some really interesting patterns from different industries and tools to consider in our concepts. </p>
          

          <p>Tuesday afternoon we sketched. The rapid-fire "Crazy-Eights" activity was torturous for some members of the team who're extremely detail-oriented, but it forced them to refine their thoughts down to the most salient and significant features. </p>



          <h4>Decide</h4>
          <p>After facilitating and participating in numerous design sprints, I'm still always surprised on "decision" day. Too often, decisions are made based on what would be easy or realistic to develop and deliver - as a final product. It's as though the team has forgotten that the purpose of a design sprint is to explore possibilities, not to start designing the final product. 
          </p>
          <p>When facilitating a design sprint, I see my role at this stage to push people to choose the unknowns. The goal of a design sprint is to learn, and so you'll get the most out of it if you ask the big questions. During this design sprint, our Decider did a great job at picking  concepts she hadn't previously considered, with a goal to maximize her learning during the week. </p>


          <h4>Interview and learn</h4>
          <p>We conducted our interviews with users of our product, as well as those of competitive products. We didn't want to risk biasing our results based on our existing product offering. One of our MBA interns conducted the interviews while the rest of the team jotted down notes on sticky notes and organized them per product feature and sentiment.</p>

          <img src="img/ds-dan.jpg" class="img-fluid" /> 


          <h4 class="mt-3">Then what?</h4>
          <p>The toughest part of a design sprint is the synthesis at the end. Everyone is exhausted and overwhelmed with information. While things were fresh in our minds, we did a high-level "WOW or MEH" rating for different aspects of the concept, and gathered our notes for later further analysis. </p>

          <p>While the official design sprint process wrapped up Friday afternoon, our team continued with the project the net week, to further synthesize our findings, and  write up recommendations and next steps. </p>




